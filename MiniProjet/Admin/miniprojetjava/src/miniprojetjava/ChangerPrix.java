    private void jLabel7MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel7MouseClicked
        // TODO add your handling code here:
        if(prix.getText().length()!=0){
       try{
           Connection conn= AdminAccueil.ConnectDB();
           Statement statement2= conn.createStatement();
           String sql2="update stock set prix="+prix.getText()+" where produit='"+prod.getSelectedItem().toString()+"' and quincaillerie='"+quin.getSelectedItem().toString()+"' ";
          int r2= statement2.executeUpdate(sql2);
           if (r2!=0){
               JOptionPane.showMessageDialog(null, "Le prix du produit "+prod.getSelectedItem().toString()+" est d√©sormais :"+prix.getText());
               prod.removeAllItems();
               prix.setText("");
               ancprix.setText("");
               quin.setSelectedIndex(-1);
            }
            statement2.close();
           }catch (SQLException e){;} 
        }
        else JOptionPane.showMessageDialog(null, "Veuillez choisir un prix");
        
    }//GEN-LAST:event_jLabel7MouseClicked

